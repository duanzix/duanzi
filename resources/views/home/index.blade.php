 <div class="post-content ">        <p>高中时，一个女孩走进了我的生活，他是我们年级的文艺委员，她看起来很纯洁。像奶茶一样甜甜的。而我却是我们班的学渣，一次次打架，一次次警告。如果不是我爸是局长，我应该早就开除了。</p><p>那个女孩叫清纯，人如其名。我疯狂的爱上了她。情人节那天晚上我买了999朵玫瑰，开着我爸的专车奥迪a8，向她表白。那晚她答应做我的女朋友，也承诺以后会做我的妻子。此后我们像大多数热恋的男女一样，寸步不离。让周围的人很羡慕。</p><p>天有不测风云，快毕业那年。我爸被查了。从高处一下跌到低处的这种心情没人体会。那天我在清纯面前哭了。清纯那晚对我说不管如何我一直会爱着你。几个月后高考结果出来，清纯考上了电影学院。而我却名落孙山。走的时候，清纯说等毕业回来就找我。我当时深信不疑。</p><p>就这样一年两年过去了，情人节前一天她发信息给我说：对不起，我有新的男朋友了。他对我很好。我也爱他。还有你这样也不是办法，在餐厅当个服务员每天混日子，你要有上进心，学点东西以后才有前途。看完信息，我心很平静，我知道这天总会来临。但是我只想见她最后一面。我买了当天晚上的火车票，想在明天的情人节做个最后的道别。</p><p>第二天我到了清纯的学校，天空飘着零星的小雨。我在学校门口等了整整一下午终于看到她的出现，她变了，整个人似乎多了点韵味。当我正要喊她的名字时候，一辆法拉利一个漂移停在了她的面前。她开车门上车的动作很自然。留下我一个人傻傻的站着，雨突然越来越大，而我却浑然不知。我迷茫，我不知何去何从。</p><p>颓废了半年，我终于在灰霾的阴影中走了出来。收拾下行李我踏上了北方的列车。</p><p>时光如梭，转眼间8年过去了。听说清纯已为人妻，似乎过的不太幸福。后来我去以发小的身份去找了她。咖啡厅里，她依旧漂亮，却多了些少妇的韵味。她说她这些年生不了孩子，公公婆婆有些不待见她，老公也想和她离婚。天天在外面花天酒天养小三。她很难过。</p><p>我此刻突然有种心疼的感觉。她接着说到：这些年你还好吗？之前对不起，你会怪我吗？我笑了笑，像以前那样摸了下她的脸蛋回答道：傻瓜，我不会怪你的，当初也是自己没用，不求进取。不过后来我听你的话，男人一定要有上进心，要有一技之长。说完我掏出了我的名片，名片上的几个字格外醒目“山东蓝翔技校挖掘机总教练”。</p>    </div>
